#include "3-calc.h"

/**
 * op_add - sum
 * @a: 1st
 * @b: 2nd *
 * Return: 1st + 2nd
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - minus
 * @a: 1st
 * @b: 2nd
 * Return:1st - 2nd 
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multibly
 * @a: 1st
 * @b: 2nd
 * Return: 1st * 2nd
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: 1st
 * @b: 2nd
 * Return: 1st / 2nd
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder
 * @a: 1st
 * @b: 2nd
 * Return: 1st % 2nd
 */

int op_mod(int a, int b)
{
	return (a % b);
}
